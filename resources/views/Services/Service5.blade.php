        <h2 class="text-2xl font-bold">Communication with Patients:</h2>
        <ul class="list-disc list-inside">
            <li><strong>Effective Communication:</strong> Use clear, empathetic language. Allow time for questions and express understanding of their feelings.</li>
            <li><strong>Psychological Counselling:</strong> Regularly assess emotional states and provide coping resources. Facilitate family meetings to encourage open dialogue.</li>
            <li><strong>Active Listening:</strong> Understand what is said by patients, families, colleagues. If not able to understand, ask and clarify. Do not assume things. Speak clearly and avoid medical jargons so that information is effectively conveyed. Read and write clearly.</li>
        </ul>
