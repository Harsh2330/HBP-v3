            <li>Do not spend more time other than that used for holistic care during a home visit.</li>
            <li>Clean hands in between procedures and when handling any medications during home care.</li>
            <li>Disinfect all instruments used for vital observations (e.g. blood pressure machines, thermometers, pulsometers, and stethoscopes).</li>
            <li>Wash hands with soap and water or use an alcohol-based sanitizer before leaving the patient’s home.</li>
